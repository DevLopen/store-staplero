// ─── Helper function ────────────────────────────────────────────────────────
export function t(key: string, lang: Language = 'de'): string {
  const keys = key.split('.');
  let value: any = translations;

  for (const k of keys) {
    value = value?.[k];
    if (!value) return key;
  }

  return value[lang] || value['de'] || key;
}

// ─── Hook ───────────────────────────────────────────────────────────────────
export function useTranslation(language: Language = 'de') {
  return (key: string) => t(key, language);
}